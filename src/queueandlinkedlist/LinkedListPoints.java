package queueandlinkedlist;

public class LinkedListPoints {
//    Linked List can be defined as collection of objects called nodes that are randomly stored in the memory.
//    A node contains two fields i.e. data stored at that particular address and the pointer
//    which contains the address of the next node in the memory.
//    The last node of the list contains pointer to the null.

//    Uses of Linked List
//    The list is not required to be contiguously present in the memory. The node can reside anywhere
//    in the memory and linked together to make a list. This achieves optimized utilization of space.
//    list size is limited to the memory size and doesn't need to be declared in advance.
//    Empty node can not be present in the linked list.
//    We can store values of primitive types or objects in the singly linked list.

//    Linked list is the data structure which can overcome all the limitations of an array.
//    Using linked list is useful because, It allocates the memory dynamically.
//    All the nodes of linked list are non-contiguously stored in the memory and
//    linked together with the help of pointers.
//    Sizing is no longer a problem since we do not need to define its size
//    at the time of declaration. List grows as per the program's demand and
//    limited to the available memory space.
}
