package arraylist;

public class ArrayListPoints {

//    Array list is like normal arrays.
//    Array has a drawback - It cannot change its size dynamically
//    Java ArrayList class uses a dynamic array for storing the elements.
//    It is like an array, but there is no size limit. We can add or remove elements anytime.
//    So, it is much more flexible than the traditional array. It is found in the java.util package.

//    The important points about the Java ArrayList class are:
//
//    Java ArrayList class can contain duplicate elements.
//    Java ArrayList class maintains insertion order.
//    Java ArrayList class is non synchronized.
//    Non-Synchronized means that two or more threads
//    can access the methods of that particular class at any given time.
//    Java ArrayList allows random access because the array works on an index basis.
//    In ArrayList, manipulation is a little
//    slower than the LinkedList in Java because a lot of shifting needs to occur
//    if any element is removed from the array list.
//    We can not create an array list of the primitive types, such as int, float, char, etc.
//    It is required to use the required wrapper class in such cases

}
